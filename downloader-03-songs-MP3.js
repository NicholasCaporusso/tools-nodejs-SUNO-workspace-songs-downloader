'use strict'
require('dotenv').config({quiet:true})

// Load environment variables for local directories and the HeyBro dependency
const FILE_HEYBRO=process.env.FILE_HEYBRO
const FOLDER_SESSION=process.env.FOLDER_SESSION
const FOLDER_DATA=process.env.FOLDER_DATA
const FOLDER_WORKSPACES=process.env.FOLDER_WORKSPACES
const FOLDER_SONGS=process.env.FOLDER_SONGS
const FILE_WORKSPACES=process.env.FILE_WORKSPACES

const fs=require('fs')
const path=require('path')
// Import the BrowserController utilized for interacting with the Suno web UI natively
const {BrowserController}=require(FILE_HEYBRO)
const sleep=ms=>new Promise(r=>setTimeout(r,ms))
let ctrl=null
let downloadTimeout=null

// Verify essential folder paths exist on filesystem to prevent crash exceptions
if(!fs.existsSync(FOLDER_WORKSPACES)) fs.mkdirSync(FOLDER_WORKSPACES,{recursive:true})
if(!fs.existsSync(FOLDER_SONGS)) fs.mkdirSync(FOLDER_SONGS,{recursive:true})

// Helper function: Recursively iterates via files matching [fileName] across [dir]'s inner tree structure.
function findFileRecursive(dir,fileName){
	const items=fs.readdirSync(dir)
	for(const item of items){
		const fullPath=path.join(dir,item)
		const stat=fs.statSync(fullPath)
		if(stat.isDirectory()){
			const result=findFileRecursive(fullPath, fileName)
			if(result) return result
		}else if(item===fileName) return fullPath
	}
	return null
}

// Helper function: Moves a file from a standalone path into a designated directory framework
function moveFile(sourcePath,destinationFolder){
	const fileName=path.basename(sourcePath)
	const destinationPath=path.join(destinationFolder,fileName)
	fs.renameSync(sourcePath,destinationPath)
}

// Spawns and configures the automated browser preserving cookies and authentication states
async function startBrowserController(){
	if(ctrl) return
	ctrl = new BrowserController({
		backend: 'puppeteer',
		browserType: 'chromium'
	});
	const sessionDir = path.resolve(FOLDER_SESSION)
	if(!fs.existsSync(sessionDir)) fs.mkdirSync(sessionDir,{recursive:true})
	await ctrl.launch({ userDataDir: sessionDir })
}

// Global Main Iterator: Inspects directories generated by downloader-02 and schedules song downloads
async function downloadAllSongs(){
	await startBrowserController()
	const workspaceFiles=fs.readdirSync(FOLDER_WORKSPACES)
	
	for(const file of workspaceFiles){
		// Skips default handling intentionally (assuming strict adherence to assigned workspaces)
		if(file!='default.json') await downloadWorkspaceSongs(file,false)
	}
	await ctrl.close()
	ctrl=null
}

// Secondary Iterator: Loops through an individual workspace fetching un-downloaded songs
async function downloadWorkspaceSongs(file,autoclose=true){
	const workspaceName=file.replace('.json','')
	
	// Create dedicated media subfolder mirroring tracking workspaces JSONs
	if(!fs.existsSync(`${FOLDER_SONGS}/${workspaceName}`)) fs.mkdirSync(`${FOLDER_SONGS}/${workspaceName}`,{recursive:true})
	
	// Load the accumulated full song JSON metadata into memory array
	const songs=Object.values(JSON.parse(fs.readFileSync(`${FOLDER_WORKSPACES}/${file}`,'utf8')))
	
	// Optimization passes check: Ensures songs physically present in system are 
	// stored in correct workspace partitions. Flags irregularities outright.
	for(const song of songs){
		const foundPath=findFileRecursive(FOLDER_SONGS,`${song.id}.mp3`)
		if(foundPath && !foundPath.includes(workspaceName)){
			console.log(foundPath)
			process.exit() // Terminate explicitly indicating data migration misalignment
		}
	}
	
	await startBrowserController()
	
	for(const song of songs){
		process.stdout.write(`Processing ${workspaceName}/${song.id}.mp3: `)
		// Skip downloading if either format is already available
		const mp3Exists = fs.existsSync(`${FOLDER_SONGS}/${workspaceName}/${song.id}.mp3`);
		const wavExists = fs.existsSync(`${FOLDER_SONGS}/${workspaceName}/${song.id}.wav`);
		
		if(!mp3Exists && !wavExists){
			console.log('downloading...')
			// Trigger download directly from CDN
			await downloadSong(workspaceName,song,false)
		}else if (wavExists) {
			console.log('already downloaded (WAV exists)')
		}else {
			console.log('already downloaded (MP3 exists)')
		}
	}
	if(autoclose) await ctrl.close()
}

// Core Task Execution: Downloads the MP3 directly from the JSON link to avoid overwhelming SUNO
async function downloadSong(workspaceName,song,autoclose=true){
	await startBrowserController()
	
    // Using the audio_url CDN direct link provided in the payload
    if (song.audio_url) {
        try {
            console.log(`Downloading ${song.audio_url} into ${FOLDER_SONGS}/${workspaceName}/${song.id}.mp3`)
            await ctrl.downloadUrl(song.audio_url, `${FOLDER_SONGS}/${workspaceName}`, `${song.id}.mp3`)
            await sleep(3000) // Sleep briefly to ensure I/O completion
        } catch(e) {
            console.error('Failed to download MP3', e)
        }
    } else {
        console.log('No MP3 audio_url provided in JSON payload.')
    }
	
	if(autoclose){
		await ctrl.close()
		ctrl=null
	}
}

// Run Script Sequence natively
if(require.main === module){
	// Parse CLI args: [workspaceId] [--delete]
	// Usage: node downloader-03-songs-MP3 [workspaceId] [--delete]
	const args = process.argv.slice(2)
	const deleteFirst = args.includes('--delete')
	const targetId = args.find(a => !a.startsWith('--'))
	
	if(targetId){
		// Single-workspace mode
		const file = `${targetId}.json`
		if(!fs.existsSync(`${FOLDER_WORKSPACES}/${file}`)){
			console.error(`Workspace file not found: ${FOLDER_WORKSPACES}/${file}`)
			process.exit(1)
		}
		if(deleteFirst){
			const audioDir = `${FOLDER_SONGS}/${targetId}`
			if(fs.existsSync(audioDir)){
				console.log(`Deleting existing MP3s in ${audioDir}`)
				fs.readdirSync(audioDir)
					.filter(f => f.endsWith('.mp3'))
					.forEach(f => fs.unlinkSync(`${audioDir}/${f}`))
			}
		}
		downloadWorkspaceSongs(file)
	} else {
		downloadAllSongs()
	}
}

module.exports = { downloadAllSongs, downloadWorkspaceSongs, downloadSong }


