'use strict'
require('dotenv').config({quiet:true})

// Load environment variables for paths and HeyBro module
const FILE_HEYBRO=process.env.FILE_HEYBRO
const FOLDER_SESSION=process.env.FOLDER_SESSION
const FOLDER_DATA=process.env.FOLDER_DATA
const FOLDER_WORKSPACES=process.env.FOLDER_WORKSPACES
const FOLDER_SONGS=process.env.FOLDER_SONGS
const FILE_WORKSPACES=process.env.FILE_WORKSPACES

const fs=require('fs')
const path=require('path')
// Import the BrowserController from the HeyBro extension for web scraping and manipulation
const {BrowserController}=require(FILE_HEYBRO)
const sleep=ms=>new Promise(r=>setTimeout(r,ms))
let ctrl=null

// Ensure workspace directory exists locally
if(!fs.existsSync(FOLDER_WORKSPACES)) fs.mkdirSync(FOLDER_WORKSPACES,{recursive:true})


// Initializes the browser using stored session data, bypassing login screens
async function startBrowserController(){
	if(ctrl) return
	ctrl=new BrowserController({
		backend:'puppeteer',
		browserType:'chromium'
	});
	const sessionDir=path.resolve(FOLDER_SESSION)
	if(!fs.existsSync(sessionDir)) fs.mkdirSync(sessionDir,{recursive:true})
	await ctrl.launch({userDataDir:sessionDir})
}

// Simulates user scrolling to the bottom to trigger XHR pagination
async function scrollToBottom(el,wait=1000){
	await ctrl.rightclickElement(el)
	await sleep(100)
	await ctrl.press('Escape')
	await sleep(100)
	await ctrl.press('Escape')
	await ctrl.press('End') // Triggers the infinite scroll loading
	await sleep(1000)
}

// Main iterator: Entrypoint to loop through each workspace sequentially
async function downloadWorkspaceIndex(){
	// Load the master workspaces index generated by the previous `downloader-01` script
	const workspaces=fs.existsSync(FILE_WORKSPACES) ? JSON.parse(fs.readFileSync(FILE_WORKSPACES,'utf8')) : {}
	if(Object.keys(workspaces).length==0){
		console.log(`No workspaces found in ${FILE_WORKSPACES}`)
		if(require.main===module) process.exit()
		return
	}
	await startBrowserController()
	const workspaceItems=Object.values(workspaces)
	console.log(`${workspaceItems.length} workspaces found.`)
	
	// Processes every individual workspace to glean song (clip) metadata
	for(const workspace of workspaceItems){
		console.log(`Downloading ${workspace.id}`)
		await downloadWorkspacesDetail(workspace,false)
	}
	await ctrl.close()
	ctrl=null
}

// Fetches the detailed list of songs (clips) belonging to a specific workspace
async function downloadWorkspacesDetail(workspace,autoclose=true){
	await startBrowserController()
	// Construct JSON output path mapped to the unique workspace UUID
	const file_workspace=`${FOLDER_WORKSPACES}/${workspace.id}.json`
	
	// Load any previously completed song data to support resuming interrupted downloads
	const songs=fs.existsSync(file_workspace) ? JSON.parse(fs.readFileSync(file_workspace,'utf8')) : {}
	
	// Open the specific workspace page URL
	let title=''
	do{
		if(ctrl.listTabs().length>1) await ctrl.closeTab()
		await ctrl.openTab(`https://suno.com/create?wid=${workspace.id}`)
		ctrl.startNetCapture()
		await sleep(3000)
		// Verifying that the correct workspace page actually loaded successfully by checking the DOM text
		title=await ctrl.getDomText('#main-container .line-clamp-1')
	}while(title!=workspace.name)
	console.log(Object.keys(songs).length,workspace.clip_count)
	// Keep fetching data until our local dictionary's elements equal the expected absolute clip_count
	while(Object.keys(songs).length!=workspace.clip_count){
		const logEntries=ctrl.getNetLog()
		for(const entry of logEntries){
			// API intercept looking for the detailed feed describing the songs
			if(entry.url.includes('/api/feed/v3') && entry.contentType=='application/json'){
				let buffer=null
				while(!buffer || buffer.length<=0){
					await sleep(500)
					buffer=ctrl.getNetBody(entry.id)
				}
				try{
					// Interpret the JSON payload containing track data
					const content=JSON.parse(buffer.toString('utf-8'))
					if(!content.clips) continue
					
					for(const clip of content.clips){
						// Ensure the song correctly maps to this exact workspace 
						// (Exceptions are made for 'default' which acts as a catch-all)
						if(workspace.id!='default' && clip.project.id!=workspace.id) continue
						// Persist song data into the dictionary natively tracking by UUID
						if(!songs[clip.id]) songs[clip.id]=clip
					}
					console.log(`Workspace ${workspace.id}: ${Object.keys(songs).length}/${workspace.clip_count}`)
					
					// Terminate internal loop when completion condition is met
					if(Object.keys(songs).length==workspace.clip_count) break
				}catch(e){}
			}
		}
		console.log(`Workspace ${workspace.id}: ${Object.keys(songs).length}/${workspace.clip_count}`)
		
		// Periodically write the accumulated song dictionary chunk back into the JSON file
		fs.writeFileSync(file_workspace,JSON.stringify(songs))
		if(Object.keys(songs).length==workspace.clip_count) break
		
		// Attempt to load the next chunk of songs by scrolling the browser layout
		try{
			await scrollToBottom('.clip-browser-list-scroller')
		}catch(e){
			console.log(e)
		}
	}
	
	// Execution finalized for this specific workspace; clean up context.
	ctrl.stopNetCapture()
	await ctrl.closeTab()
	if(autoclose){
		await ctrl.close()
		ctrl=null
	}
}

// Execute the download logic
if(require.main === module){
	// Parse CLI args:  [workspaceId] [--delete]
	// Usage: node downloader-02 [workspaceId] [--delete]
	const args = process.argv.slice(2)
	const deleteFirst = args.includes('--delete')
	const targetId = args.find(a => !a.startsWith('--'))
	
	if(targetId){
		// Single-workspace mode
		const workspaces = fs.existsSync(FILE_WORKSPACES) ? JSON.parse(fs.readFileSync(FILE_WORKSPACES,'utf8')) : {}
		const workspace = workspaces[targetId]
		if(!workspace){
			console.error(`Workspace '${targetId}' not found in ${FILE_WORKSPACES}`)
			process.exit(1)
		}
		if(deleteFirst){
			const file = `${FOLDER_WORKSPACES}/${targetId}.json`
			if(fs.existsSync(file)){
				console.log(`Deleting ${file}`)
				fs.unlinkSync(file)
			}
		}
		downloadWorkspacesDetail(workspace)
	} else {
		downloadWorkspaceIndex()
	}
}

module.exports = { downloadWorkspaceIndex, downloadWorkspacesDetail }